"""14. Write a Python program to calculate the number of days between two dates.
Sample dates : (2014, 7, 2), (2014, 7, 11)
Expected output : 9 days  """
fecha1 = [2014,7,2]
fecha2 = [2014,7,11]

print("Diferencia de años",fecha2[-3] - fecha1[-3])
print("Diferencia de meses",fecha2[-2] - fecha1[-2])
print("Diferencia de días",fecha2[-1] - fecha1[-1])